package ar.edu.unlam.alumno.pb2.profugo;

public class Profugo {
	private String nombreProfugo;
	private Integer inocencia;
	private Integer habilidad;
	private Boolean esNervioso;

	public Profugo(String nombreProfugo, Integer inocencia, Integer habilidad, Boolean esNervioso) {
		this.nombreProfugo = nombreProfugo;
		this.inocencia = inocencia;
		this.habilidad = habilidad;
		this.esNervioso = esNervioso;
	}

}
